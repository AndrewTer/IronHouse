<h3 align="center">Параметры выбора шлагбаума</h3>
<div id="block-art">
<p class="articsP">Одним из средств, контролирующих поток автомобилей или пешеходов на ту или иную территорию, является шлагбаум.</p>
<p align="center" style="font: 18px sans-serif; color: #34659D;">Что необходимо учитывать при выборе шлагбаума?</p>
<ul id="articUL">
<li>Точная ширина проезда</li>
<p class="articsP">Укоротить или, наоборот, удлинить стрелу шлагбаума невозможно, поэтому необходимо знать точные размеры, чтобы шлагбаум эффективно выполнял свои функции. Если на территорию объекта постоянно заезжает транспорт, следует выбирать шлагбаумы с высокой пропускной способностью, которая обуславливается временем подъема и опускания стрелы. В зависимости от назначения и модели время подъема и опускания стрелы длится от 2-ух до 10 секунд. Поэтому, чем быстрее работает стрела, тем выше пропускная способность.</p>
<li>Тип привода шлагбаума</li>
<p class="articsP">В зависимости от типа привода существуют гидравлические и электромеханические шлагбаумы. К достоинствам гидравлического типа относится бесшумность и бесперебойность режима работы. К минусам можно отнести затруднение работы в мороз, так как масло, обеспечивающее работу гидропривода, замерзает.
<br /><br />
Электромеханические являются самым оптимальным видом шлагбаумов. Они просты в эксплуатации и могут работать при любых погодных условиях.</p>
<li>Способ управления шлагбаумом</li>
<p class="articsP">Управление шлагбаумом может осуществляться с помощью:</p>
<ul>
<li>выносного ключа-выключателя;</li>
<li>клавиатуры;</li>
<li>радиоканала;</li>
<li>простой кнопки.</li>
</ul><br />
<li>Устройства, обеспечивающие безопасность</li>
<p class="articsP">
<b style="font: 17px sans-serif; color: #34659D;">Проблесковые лампы</b> предупреждают о начале движения стрелы.
<br />
<b style="font: 17px sans-serif; color: #34659D;">Фотоэлементы безопасности</b> устанавливаются на выносных стойках. Блокируют стрелу шлагбаума в ситуациях, когда в область его действия попадают автомобили и люди. Это позволяет предотвратить аварийные ситуации. Зона действия фотоэлементов может достигать 30 метров.
<br />
<b style="font: 17px sans-serif; color: #34659D;">Бордюры безопасности</b> располагаются внизу стрелы. В случае, когда автомобиль врезается в шлагбаум, бордюры смягчают удар.
<br />
<b style="font: 17px sans-serif; color: #34659D;">Катафорты</b> – это светоотражающие накладки, устанавливающиеся на стрелу шлагбаума, что делает его заметным в темноте.
<br />
Учитывая все вышеописанные параметры, Вы сможете выбрать идеально подходящий шлагбаум.
</p>
