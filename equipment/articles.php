<h3 align="center">Интенсивность шлагбаума</h3>
<div id="block-art">
<img src="http://localhost/ironhouse/equipment/images/sh.jpg" style="float: left; width: auto; margin: 15px 45px 15px 15px; border: 3px solid #F5F5F5; box-shadow: 0.6em 0.6em 7px #C5C5C5;" />
<p class="articsP">Шлагбаумы имеют условное разделение на 2 типа:</p>
<ul id="articUL">
<li>малой интенсивности (residential)</li>
<li>интенсивные (industrial)</li>
</ul>
<p class="articsP">Первые предназначены для нагрузки от ста до нескольких сотен циклов в день, вторые - для нагрузки несколько тысяч циклов. Разные производители указывают в качестве рекомендованной разную суточную интенсивность работы шлагбаума, поэтому сравнивать модели разных производителей только по одному этому параметру достаточно сложно.</p>
<p class="articsP">Отдельные производители указывают в технических характеристиках параметр средней наработки на отказ - MCBF (MiddleCyclesBeforeFailure), который и является показателем надежности. Данный параметр является стандартизованным, однако его указывают не все производители. Так, шлагбаум, имеющий MCBF 1 млн. циклов - достаточно надежен, а MCBF 5 млн. - более чем надежен.</p>
